<?php
// 1. เริ่ม Session (ต้องอยู่บรรทัดบนสุด)
// เพื่อให้สามารถเข้าถึงและจัดการกับ Session ที่กำลังทำงานอยู่
session_start();

// 2. ล้างค่าตัวแปรใน Session ทั้งหมด
// (เช่น $_SESSION['mem_id'], $_SESSION['fullname'] จะถูกลบออกไป)
session_unset();

// 3. ทำลาย Session file บนเซิร์ฟเวอร์
// นี่คือการ "ฆ่า" session นั้นทิ้งอย่างสมบูรณ์
session_destroy();

// 4. ส่งผู้ใช้กลับไปหน้าล็อกอิน
// (คุณสามารถเปลี่ยน 'login.html' ให้เป็นชื่อไฟล์ฟอร์มล็อกอินของคุณ)
header('Location: Login.php');

// 5. หยุดการทำงานของสคริปต์ทันที
// (สำคัญมาก: ควรเรียก exit() หลังจาก header() เสมอ)
exit();

?>